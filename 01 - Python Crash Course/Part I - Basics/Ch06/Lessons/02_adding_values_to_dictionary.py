print("Exercise 2 - Adding Values to a Dictionary")

alien_0 = {"color" : "green", "points" : 5}
print(alien_0)

# Add a new value by simply populating a new key
alien_0["x_position"] = 0
alien_0["y_position"] = 25
print(alien_0)

# You can also start with an empty dictionary and add values as you need them
alien_0 = {}
alien_0["color"] = "blue"
alien_0["points"] = 10
print(alien_0)
