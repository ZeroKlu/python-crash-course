print("Exercise 7 - Looping Dictionary Items")

user_0 = {
    "username": "efermi",
    "first": "enrico",
    "last": "fermi",
}

# You can loop through a dictionary using the "items" list of key value pairs
for key, value in user_0.items():
    print(f"\nKey:   {key}")
    print(f"Value: {value}")
