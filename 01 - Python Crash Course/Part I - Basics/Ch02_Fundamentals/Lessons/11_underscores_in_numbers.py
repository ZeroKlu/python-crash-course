print("Chapter 2:")
print("Exercise 11 - Underscores for grouping")

# In any numerical value, you can include underscores
#   (which are ignored by the Python interpreter)
# They can go anywhere in the number, but are most frequently used
#   where commas would be for grouping
# e.g.:     1000 = 1_000 = 10_00, etc.
universe_age = 14_000_000_000
print(universe_age)
