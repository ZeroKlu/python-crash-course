print("Chapter 2:")
print("Exercise 3 - Strings")

# You can enclose a "string" in double-quotes
message = "This is a string"
print(message)

# You can also enclose a 'string' in single-quotes
message = 'This is also a string'
print(message)

# When you enclose with single-quotes, double-quotes are automatically interpreted as literals
message = 'I told my friend, "Python is my favorite language!"'
print(message)

# When you enclose with double-quotes, single-quotes are automatically interpreted as literals
message = "The language 'Python' is named after Monty Python, not the snake."
print(message)
